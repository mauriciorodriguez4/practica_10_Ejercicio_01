//Boton para agregar los diferentes sabores a la lista
    private void btnSaborActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // Boton agregar sabor
        if (!this.txtSabor.getText().trim().isEmpty()) {
            
            saborL.add(new Sabor(this.txtSabor.getText()));
            this.txtSabor.setText("");
            llenarLista();
            
        }
    }          
  
//Boton para agregar los conos en la tabla
    private void btnConoActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // Boton agregar cono
        if (!this.txtCono.getText().trim().isEmpty() && !this.txtPrecio.getText().trim().isEmpty()) {
            
            conoL.add(new Cono (this.txtCono.getText().trim(), Double.valueOf(this.txtPrecio.getText())));
            this.txtCono.setText("");
            this.txtPrecio.setText("");
            llenarTabla();
        }
    }                                       
    //Metodo para llenar la tabla con valores
    private void llenarTabla(){
        
        Comparador cmp = new Comparador(){
            @Override
            public int compare(Ingredientes o1, Ingredientes o2){
                return((Cono)o1).getPrecio1()-((Cono)o2).getPrecio1();
            }
        };
        
        conoL.sort(cmp);
        modelT= new DefaultTableModel();
        modelT.addColumn("Nombre");
        modelT.addColumn("Precio");
        for (int i = 0; i < conoL.size(); i++) {
            Object [] a = new Object [2];
            a[0]= conoL.get(i).getNombre();
            a[1]= ((Cono)conoL.get(i)).getPrecio();
            modelT.addRow(a);
        }
        
        this.tbCono.setModel(modelT);
        
    }
    //Metodo para llenar la lista de sabores
    private void llenarLista(){
        modelL = new DefaultListModel();
        Comparador cmp = new Comparador(){
            public int compare(Ingredientes o1, Ingredientes o2){
                return o1.getNombre().compareTo(o2.getNombre());
            }
        };
        
        saborL.sort(cmp);
        for (int i = 0; i < saborL.size(); i++)modelL.add(i, saborL.get(i).getNombre());
        this.lSabor.setModel(modelL);
            
        
    }
